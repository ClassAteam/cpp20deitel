// Reading in command-line arguments.
#include <iostream>
using namespace std;

int main(int argc, char* argv[]) {
   cout << "There were " << argc << " command-line arguments:\n";
   for (int i{0}; i < argc; ++i) {
      cout << argv[i] << endl;
   }
}


